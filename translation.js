// Helper function — use in all HTML files
// Example: t('scan_btn') returns translated button text
function t(key) {
  const lang = getCurrentLang();
  const dict = TRANSLATIONS[lang] || TRANSLATIONS['en'];
  return dict[key] || TRANSLATIONS['en'][key] || key;
}

function getCurrentLang() {
  try { return localStorage.getItem("annadatahub_lang") || "en"; } catch(e) { return "en"; }
}

function applyTranslations() {
  const lang = getCurrentLang();
  // Apply to all elements with data-t attribute
  // Example: <button data-t="scan_btn">Scan for Disease</button>
  document.querySelectorAll("[data-t]").forEach(el => {
    const key = el.getAttribute("data-t");
    const translation = t(key);
    if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
      el.placeholder = translation;
    } else {
      el.textContent = translation;
    }
  });
}
